
# crea lista vuota
pino = []  # creiamo una lista vuota

a = 1  # diciamo che la variabile è = 1

while a < 10:  # mentre la variabile è minore di 10
    pino.append(a)  # aggiungiamo l'elemento alla lista
    a = a+1  # la variabile aumenta di 1
    print(pino)  # scrive la lista corrente ad ogni iterazione # fino al numero 9




